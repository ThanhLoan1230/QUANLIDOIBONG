    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         try {
            Luong dao = new Luong();
            String a;
            if (jComboBox1.getSelectedIndex() == 0) {
                a = "ma";
            } else if (jComboBox1.getSelectedIndex() == 1) {
                a = "hovaten";
            } else if (jComboBox1.getSelectedIndex() == 2) {
                a = "luong";
            } else if (jComboBox1.getSelectedIndex() == 3) {
                a = "vaitro";
            } else {
                a = "luong";
            }
            String b = txtTimkiem.getText();
            List<LuongMOD> list = new Luong().findBy(a, b);
            System.out.println("list size = " + list.size());

            if (list.size() != 0) {
               
                try {

                    tblModel.setRowCount(0);

                    for (LuongMOD it : list) {
                        tblModel.addRow(new Object[]{
                            it.getMa(), it.getHovaTen(), it.getLuong(), it.getVaiTro()
                        });
                    }
                    tblModel.fireTableDataChanged();
                } catch (Exception e) {
                    e.printStackTrace();
                    MessagaDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi !!!!");
                }

                txtMaFocusLost(null);
                
            } else {
                MessagaDialog.showMessageDialog(parentForm, " Không tìm thấy thông tin  !", "Thông báo ");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessagaDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗii");
        }
    }//GEN-LAST:event_jButton4ActionPerformed
