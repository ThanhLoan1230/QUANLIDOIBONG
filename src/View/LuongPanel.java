    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
          StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMa, sb, "Mã  Không Được Để Trống !");
        DataValidator.validateEmpty(txtName, sb, "Tên Không Được Để Trống !");

        if (sb.length() > 0) {
            MessagaDialog.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        if (MessagaDialog.showConfirmDialog(parentForm, "Bạn có muốn chỉnh sửa thông tin  nảy không ? ", "Thống Bào") == JOptionPane.NO_OPTION) {
            return;
        }

        try {
            LuongMOD l = new LuongMOD();

           l.setMa(txtMa.getText());
            l.setHovaTen(txtName.getText());
            l.setLuong(txtLuong.getText());
            l.setVaiTro(txtVaiTro.getText());
            
            Luong dao = new Luong();
            if (dao.update(l)) {
                MessagaDialog.showMessageDialog(parentForm, "Bạn đã chỉnh sửa thành công !", "Thông bào");
                loadDataToTable();
            } else {
                MessagaDialog.showConfirmDialog(parentForm, "Chỉnh Sửa Thông Tin Không Thành Công", "Lỗi");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessagaDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMa, sb, "Mã Không Được Để Trống !");

        if (sb.length() > 0) {
            MessagaDialog.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        if (MessagaDialog.showConfirmDialog(parentForm, "Bạn Có Muốn Xóa Thông Tin Này Không ? ", "Thông Báo") == JOptionPane.NO_OPTION) {
            return;
        }

        try {

            Luong dao = new Luong();
            if (dao.delete(txtMa.getText())) {
                MessagaDialog.showMessageDialog(parentForm, "Xóa  Thành Công!", "Thông Báo");
                btnResetActionPerformed(evt);
                loadDataToTable();
            } else {
                MessagaDialog.showConfirmDialog(parentForm, "Không Thể Xóa !", "Lỗi");
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessagaDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

