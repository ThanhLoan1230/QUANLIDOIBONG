    private void initTable() {
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new String[]{"Mã HLV", "Tên HLV", "Ngày Sinh", "Quê Quán", "CMND", "Email", "Chức Vụ", "SDT"});
        jTable2.setModel(tblModel);
    }
    
    private void loadDataToTable() {
        try {
            HLV dao = new HLV();
            List<HLVMOD> list = dao.findAll();
            tblModel.setRowCount(0);
            for (HLVMOD it : list) {
                tblModel.addRow(new Object[]{
                    it.getMaHLV(), it.getTenHVL(), it.getNgaySinh(), it.getQueQuan(), it.getCMND(), it.getEmail(), it.getChucVu(), it.getSDT()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessagaDialog.showErrorDialog(parentForm, e.getMessage(), "Fail");
        }
    }
