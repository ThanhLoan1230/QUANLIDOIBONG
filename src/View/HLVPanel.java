    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
         StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaHLV, sb, "Mã Cầu Thủ Không Được Để Trống !");
        DataValidator.validateEmpty(txtTenHLV, sb, "Tên Cầu Thủ Không Được Để Trống !");

        if (sb.length() > 0) {
            MessagaDialog.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try {
            HLVMOD hlv = new HLVMOD();

            hlv.setMaHLV(txtMaHLV.getText());
            hlv.setTenHVL(txtTenHLV.getText());
            hlv.setNgaySinh(txtNgaySinh.getText());
            hlv.setQueQuan(txtQueQuan.getText());
            hlv.setCMND(txtCMND.getText());
            hlv.setEmail(txtEmail.getText());
            hlv.setChucVu(txtChucVu.getText());
            hlv.setSDT(txtSDT.getText());

            HLV dao = new HLV();
            if (dao.insert(hlv)) {
                MessagaDialog.showMessageDialog(parentForm, "Bạn Có Muốn Thêm Thông Tin Cầu Thủ Này Không !", "Thông Báo");
                loadDataToTable();
            } else {
                MessagaDialog.showConfirmDialog(parentForm, "Thêm Thông Tin Không Thành Công", "Lỗi");
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessagaDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }

            
        
    }//GEN-LAST:event_btnThemActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        try {
            
            int row = jTable2.getSelectedRow();

            if (row >= 0) {
                String id = (String) jTable2.getValueAt(row, 0);
                HLV dao = new HLV();
                HLVMOD hlv = dao.findByID(id);

                if (hlv != null) {
                    txtMaHLV.setText(hlv.getMaHLV());
                    txtTenHLV.setText(hlv.getTenHVL());
                    txtNgaySinh.setText(hlv.getNgaySinh());
                    txtQueQuan.setText(hlv.getQueQuan());
                    txtCMND.setText(hlv.getCMND());
                    txtEmail.setText(hlv.getEmail());
                    txtChucVu.setText(hlv.getChucVu());
                    txtSDT.setText(hlv.getSDT());
                    
                    
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessagaDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jTable2MouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
         StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaHLV, sb, "Mã HLV Không Được Để Trống !");

        if (sb.length() > 0) {
            MessagaDialog.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        if (MessagaDialog.showConfirmDialog(parentForm, "Bạn Có Muốn Xóa HLV Này Không ? ", "Thông Báo") == JOptionPane.NO_OPTION) {
            return;
        }

        try {

            HLV dao = new HLV();
            if (dao.delete(txtMaHLV.getText())) {
                MessagaDialog.showMessageDialog(parentForm, "Bạn Đã Xóa Thành Công !", "Thông Báo");
                btnXoa1ActionPerformed(evt);
                loadDataToTable();
            } else {
                MessagaDialog.showConfirmDialog(parentForm, "Không Xóa Được !", "Lỗi");
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessagaDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

